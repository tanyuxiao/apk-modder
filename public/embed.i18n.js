function resolveLocale(raw) {
  const value = String(raw || '').toLowerCase();
  if (value.startsWith('zh-tw') || value.startsWith('zh-hk')) return 'zh-TW';
  if (value.startsWith('zh')) return 'zh-CN';
  if (value.startsWith('ja')) return 'ja';
  if (value.startsWith('th')) return 'th';
  if (value.startsWith('en')) return 'en';
  return 'zh-CN';
}

export function createI18n(defaultLocale = 'zh-CN') {
  let locale = resolveLocale(defaultLocale);

  function t(key) {
    const bundle = I18N[locale] || I18N['zh-CN'];
    return bundle[key] || I18N.en[key] || key;
  }

  function setLocale(next) {
    locale = resolveLocale(next);
  }

  function getLocale() {
    return locale;
  }

  return {
    t,
    setLocale,
    getLocale,
    resolveLocale,
  };
}
